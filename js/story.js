/*
 * ストーリーデータ
 *
 * ■ 章の追加方法：
 *   1. chapters 配列に { id, label, title } を追加
 *   2. scenes にシーンを追加し、既存シーンの next で接続
 *
 * ■ シーン定義：
 *   chapter     : 章番号（chapters の id）
 *   text        : テキスト配列。{話者名}で話者表示
 *   next        : 次のシーンID（テキスト完了後に自動遷移）
 *   nextConditions: 条件付き遷移先 [{ condition?, next }]（next より優先）
 *   choices     : 選択肢 [{ text, next, condition?, choiceId? }]
 *   puzzle      : パズル { id?, question, answers[], failText,
 *                          successNext?, successConditions?[] }
 *   flag        : シーン到達時にセットするフラグ名
 *   clue        : 手がかり { id, text }
 *   hints       : ヒント配列（段階表示）
 *   showChapter : true → チャプターカード演出
 *   isEnding    : true → エンディング画面へ遷移
 *   endingType  : "true" | "normal" | "bad" | "secret"
 *   suspicion   : number → 違和感レベル増減
 *   memory      : { id, text, chapter? } → 記憶の断片を追加
 *   npcSuspicion: string → 違和感演出テキスト通知
 *   meta        : { text } → メタナラティブ割り込み（{name}=プレイヤー名、{time}=現在時刻）
 *
 * ■ condition 構文：
 *   "flag_name"         フラグがセット済み
 *   "!flag_name"        フラグが未セット
 *   "clue:clue_id"      手がかりを所持
 *   "!clue:clue_id"     手がかりを未所持
 *   "choice:choice_id"  選択が記録済み
 *   "suspicion:N"       違和感レベル >= N
 *   "!suspicion:N"      違和感レベル < N
 *   "memory:id"         記憶の断片を所持
 *   "!memory:id"        記憶の断片を未所持
 *   複数条件は "&&" で結合
 *
 * ■ パズル条件分岐（successConditions）：
 *   配列を上から評価し、最初に condition を満たす next へ遷移
 *   condition 省略 = デフォルト（必ず末尾に置く）
 */

